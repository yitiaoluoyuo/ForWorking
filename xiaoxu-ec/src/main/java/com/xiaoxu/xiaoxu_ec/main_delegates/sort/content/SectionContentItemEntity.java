public class SectionContentItemEntity {

    private int mId;
    private String mSubtitle;
    private String mMainImage;
    private String mImageHost;


    public int getId() {
        return mId;
    }

    public void setId(int mId) {
        this.mId = mId;
    }

    public String getSubtitle() {
        return mSubtitle;
    }

    public void setSubtitle(String mSubtitle) {
        this.mSubtitle = mSubtitle;
    }

    public String getMainImage() {
        return mMainImage;
    }

    public void setMainImage(String mMainImage) {
        this.mMainImage = mMainImage;
    }

    public String getImageHost() {
        return mImageHost;
    }

    public void setImageHost(String mImageHost) {
        this.mImageHost = mImageHost;
    }
}
